package BuilderPattern;

public class Laptop {
    String ram;
    String Processor;
    String touchPad;
    String Monitor;

    public void setRam(String ram) {
        this.ram = ram;
    }
    public void setMonitor(String monitor) {
        Monitor = monitor;
    }
    public void setProcessor(String processor) {
        Processor = processor;
    }
    public void setTouchPad(String touchPad) {
        this.touchPad = touchPad;
    }
}
